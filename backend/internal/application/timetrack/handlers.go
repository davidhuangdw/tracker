package timetrack

import (
	"net/http"
	"strconv"
	"time"

	"example.com/tracker/internal/domain/timetrack"
	"github.com/gin-gonic/gin"
)

type Handlers struct {
	service Service
}

func NewHandlers(service Service) *Handlers {
	return &Handlers{
		service: service,
	}
}

// Activity handlers
func (h *Handlers) CreateActivity(c *gin.Context) {
	var activity timetrack.Activity
	if err := c.ShouldBindJSON(&activity); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
		return
	}

	// Set default user ID for now
	activity.UserID = 1

	if err := h.service.CreateActivity(&activity); err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}

	c.JSON(http.StatusCreated, activity)
}

func (h *Handlers) GetActivity(c *gin.Context) {
	id, err := strconv.Atoi(c.Param("id"))
	if err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": "Invalid ID"})
		return
	}

	activity, err := h.service.GetActivityByID(id)
	if err != nil {
		c.JSON(http.StatusNotFound, gin.H{"error": "Activity not found"})
		return
	}

	c.JSON(http.StatusOK, activity)
}

func (h *Handlers) GetActivities(c *gin.Context) {
	userID := 1 // Default user ID

	// Parse from and to parameters
	fromStr := c.Query("from")
	toStr := c.Query("to")

	from, err := time.Parse(time.RFC3339, fromStr)
	if err != nil {
		from = time.Now().AddDate(0, 0, -7) // Default to 7 days ago
	}

	to, err := time.Parse(time.RFC3339, toStr)
	if err != nil {
		to = time.Now() // Default to now
	}

	activities, err := h.service.GetActivitiesByUserID(userID, from, to)
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}

	c.JSON(http.StatusOK, activities)
}

